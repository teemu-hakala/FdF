#include "libft.h"
#include <stdlib.h>

t_list	*ft_lstnew(void const *content, size_t content_size)
{
	t_list	*new;
	void	*mem;

	new = (t_list *)malloc(sizeof(t_list));
	if (!new)
		return (NULL);
	new->next = NULL;
	if (!content)
	{
		new->content = NULL;
		new->content_size = 0;
		return (new);
	}
	mem = ft_memdup(content, content_size);
	if (!mem)
	{
		free(new);
		return (NULL);
	}
	new->content = mem;
	new->content_size = content_size;
	return (new);
}
